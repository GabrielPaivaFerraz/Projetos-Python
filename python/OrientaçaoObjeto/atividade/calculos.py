class Calculo:
    
    class Circulo():
        def area(self, a, b,):
            self.a = a
            self.b = b
       
        def areacirculo(self):
            self.resultado =  3,14 * 2
            return  self.resultado

    class Retangulo():
    
        def area(self, a, b):
            self.a = a
            self.b = b

        def areaRetangulo(self, a, b):
            self.resultado = a * b
            return self.resultado        

    class Triangulo():

        def area(self, a, b, c, ):
            self.a = a
            self.b = b
            self.c = c 
      

        def areaTriangulo(self, a, b, c):
            self.resultado = a * b / c
            return self.resultado        